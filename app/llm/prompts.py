"""Промпты для LLM-задач. Версионируем — старые сохраняются в llm_runs.prompt_version,
чтобы можно было сравнить v1 vs v2 на одной и той же вакансии.
"""

from __future__ import annotations

REQUIREMENTS_V1 = "requirements_v1"

_REQ_SYSTEM_V1 = """Ты строгий парсер описаний IT-вакансий. Извлекаешь из текста только конкретные требования.

Возвращай ТОЛЬКО валидный JSON без markdown-обёрток и без объяснений.
Если поля нет — давай пустой массив. Не выдумывай отсутствующее.
""".strip()

_REQ_USER_TEMPLATE_V1 = """Разбери описание вакансии. Извлеки список конкретных требований и сгруппируй по строгости и категории.

Категории строгости (kind):
- "must": обязательные требования ("требуется", "необходимо", "обязательно", "вы должны", "ждём от вас")
- "nice": желательно ("желательно", "приветствуется", "приятным дополнением")
- "plus": будет плюсом ("будет плюсом", "плюсом будет", "nice to have")

Категории смысла (category):
- "stack": технология/инструмент (Python, Django, PostgreSQL, Kubernetes, Kafka, React, AWS, Linux ...)
- "exp": опыт ("3+ года Python", "опыт highload", "production experience")
- "soft": soft skills ("коммуникабельность", "работа в команде", "проактивность")
- "edu": образование ("высшее техническое", "степень магистра")
- "other": всё остальное (методологии, английский на уровне, готовность к командировкам и т.п.)

Формат ответа:
{{
  "requirements": [
    {{"kind": "must", "category": "stack", "text": "Python 3.10+"}},
    {{"kind": "must", "category": "exp", "text": "5+ лет коммерческой разработки на Python"}},
    {{"kind": "nice", "category": "stack", "text": "Kubernetes"}},
    {{"kind": "plus", "category": "other", "text": "Английский B2+"}}
  ]
}}

Правила извлечения:
1. Один пункт = одна сущность. Если в исходнике "Django, FastAPI, Flask" — разбей на 3 элемента (kind/category одинаковые).
2. Текст оставляй кратким (1-6 слов), близко к оригиналу. Версии (Python 3.10+) сохраняй.
3. Если строгость неочевидна — ставь "must" (по умолчанию это требования).
4. Если категория неочевидна — "other".
5. Дубликаты не выдавай.

Описание вакансии:
---
{description}
---""".strip()


def requirements_prompt_v1(description: str) -> tuple[str, str, str]:
    """Возвращает (version, system, user)."""
    return REQUIREMENTS_V1, _REQ_SYSTEM_V1, _REQ_USER_TEMPLATE_V1.format(description=description)
